// 📚 变量交换的5种方法
// 学习点：不同的变量交换技巧

let a = 5, b = 10;
console.log('原始值：', 'a =', a, 'b =', b);

// 方法1：使用临时变量（最常见）
let temp = a;
a = b;
b = temp;
console.log('方法1（临时变量）：', 'a =', a, 'b =', b);

// 方法2：使用加减法
a = 5, b = 10;
a = a + b; // a = 15
b = a - b; // b = 5
a = a - b; // a = 10
console.log('方法2（加减法）：', 'a =', a, 'b =', b);

// 方法3：使用ES6解构赋值（最优雅）
a = 5, b = 10;
[a, b] = [b, a];
console.log('方法3（解构赋值）：', 'a =', a, 'b =', b);

// 方法4：使用异或运算（位运算）
a = 5, b = 10;
a = a ^ b;
b = a ^ b;
a = a ^ b;
console.log('方法4（异或运算）：', 'a =', a, 'b =', b);

// 方法5：使用数组
a = 5, b = 10;
a = [b, b = a][0];
console.log('方法5（数组技巧）：', 'a =', a, 'b =', b);
